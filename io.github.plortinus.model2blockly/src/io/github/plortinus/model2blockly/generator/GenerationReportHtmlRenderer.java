	private static boolean hasStandalone(Collection<String> generatedFiles) {
		for (String file : generatedFiles) {
			if (file != null && file.endsWith("_standalone.html")) return true;
		}
		return false;
	}

	private static boolean hasSampleModel(Collection<String> generatedFiles) {
		for (String file : generatedFiles) {
			if (file != null && file.endsWith("sample_model.json")) return true;
		}
		return false;
	}

