	private static void appendEcoreMapping(StringBuilder out, EPackage pkg, EditorSpec spec) {
		out.append("<h2>Metamodel to Blockly Mapping</h2>\n");
		Map<String, BlockTypeSpec> blockMap = blockMap(spec);
		tableStart(out, "EClass", "Generated block", "Role", "Fields / references");
		for (EClass eClass : allEClasses(pkg)) {
			BlockTypeSpec bt = blockMap.get(eClass.getName());
			if (bt == null) {
				row(out, eClass.getName(), "❌ Not generated", "❌ Skipped", "-");
			} else {
				row(out, eClass.getName(), "✅ " + bt.getTypeName(), role(bt),
					"✅ " + bt.getFields().size() + " fields, "
						+ bt.getStatementInputs().size() + " containments, "
						+ bt.getReferenceFields().size() + " references");
			}
		}
		tableEnd(out);
	}

