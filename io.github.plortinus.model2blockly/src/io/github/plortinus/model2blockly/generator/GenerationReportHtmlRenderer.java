	private static void appendStats(StringBuilder out, String[][] stats) {
		out.append("<section class=\"stats\">\n");
		for (String[] stat : stats) {
			out.append("<div class=\"stat\"><div class=\"label\">")
				.append(escape(stat[0]))
				.append("</div><div class=\"value\">")
				.append(escape(value(stat[1])))
				.append("</div></div>\n");
		}
		out.append("</section>\n");
	}

	private static void appendGeneratedFiles(StringBuilder out, Collection<String> generatedFiles) {
		out.append("<h2>Generated Output</h2>\n");
		tableStart(out, "File", "Purpose");
		for (String file : generatedFiles) {
			if (isKeyOutput(file)) rowLinked(out, file, purpose(file));
		}
		rowLinked(out, "README.md", "✅ Short instructions inside the output folder");
		rowLinked(out, "generation_report.html", "✅ Concise human-readable report");
		tableEnd(out);
	}

	private static boolean isKeyOutput(String file) {
		return isIntermediateXmi(file)
			|| file.endsWith("_standalone.html")
			|| file.endsWith("_editor.html")
			|| file.endsWith("_validations.js")
			|| file.endsWith("_blocks.js")
			|| file.endsWith("validation_workspace.html")
			|| file.endsWith("validation_blocks.json")
			|| file.endsWith("validationBlocks.json")
			|| file.endsWith("validation_runtime.js")
			|| file.endsWith("validationRuntime.js")
			|| file.endsWith("sample_model.json")
			|| file.startsWith("html/");
	}

	private static boolean isIntermediateXmi(String file) {
		return file != null
			&& file.startsWith("intermediate/")
			&& file.endsWith("_blocklyspec.xmi");
	}

	private static String findIntermediateXmi(Collection<String> generatedFiles) {
		for (String file : generatedFiles) {
			if (isIntermediateXmi(file)) return file;
		}
		return null;
	}

	private static void appendGenerationPipeline(StringBuilder out, String sourceKind,
			Collection<String> generatedFiles) {
		String intermediate = findIntermediateXmi(generatedFiles);
		out.append("<h2>Generation Pipeline</h2>\n");
		tableStart(out, "Step", "Artifact", "Meaning");
		row(out, "1. Source model", sourceKind,
			"The selected model is adapted into the shared generator-facing model.");
		row(out, "2. Intermediate EMF model", "EditorSpec",
			"Both the DSL and Ecore routes normalize into the same model shape.");
		rowPipelineLinked(out, "3. Intermediate XMI",
			intermediate != null ? intermediate : "intermediate/*_blocklyspec.xmi",
			intermediate != null
				? "The generator writes this XMI, reloads it, and validates the reloaded model."
				: "The generator writes intermediate XMI, reloads it, and validates the reloaded model.");
		row(out, "4. Blockly editor", "html/*",
			"HTML, JavaScript, validation runtime and report files are emitted from the reloaded EditorSpec model.");
		row(out, "5. Domain instance model", "*_model.xmi",
			"The generated editor can export user-created blocks as EMF-style XMI in the source domain namespace.");
		tableEnd(out);
	}

	private static void appendEcoreQualitySummary(StringBuilder out, EPackage pkg,
			EditorSpec spec, Collection<String> generatedFiles) {
		out.append("<h2>Generation Quality Summary</h2>\n");
		tableStart(out, "Check", "Status", "Meaning");
		row(out, "Editor generated",
			hasStandalone(generatedFiles) ? "✅ Yes" : "❌ No",
			hasStandalone(generatedFiles)
				? "A standalone Blockly editor is available from this report."
				: "No standalone editor was listed in the generated files.");
		row(out, "Sample model generated",
			hasSampleModel(generatedFiles) ? "✅ Yes" : "❌ No",
			hasSampleModel(generatedFiles)
				? "`sample_model.json` and the `Load Sample` button are available."
				: "The user must build the first model manually.");
		row(out, "Sample import support",
			hasStandalone(generatedFiles) && hasSampleModel(generatedFiles) ? "✅ Available" : "❌ Missing",
			"The generated editor embeds the sample model and can load it without file selection.");
		row(out, "Domain instance XMI export",
			hasStandalone(generatedFiles) ? "✅ Available" : "❌ Missing",
			"The generated editor can export created models as EMF-style XMI using EPackage nsURI/nsPrefix metadata.");
		row(out, "Generated validations",
			spec.getValidations().isEmpty() ? "⚠️ None" : "✅ " + spec.getValidations().size(),
			spec.getValidations().isEmpty()
				? "No cardinality, required, uniqueness or expression validations were generated."
				: "Validation rules are generated into the editor runtime.");
		int unsupported = unsupportedEcoreConstructCount(pkg);
		row(out, "Unsupported or partial Ecore constructs",
			unsupported == 0 ? "✅ None detected" : "⚠️ " + unsupported + " detected",
			unsupported == 0
				? "This input does not use the main constructs that remain partial or unsupported."
				: "See `Ecore Coverage Matrix`, `Recommended Improvements` and `What To Check` below.");
		row(out, "EOperations",
			operationCount(pkg) == 0 ? "✅ None detected" : "❌ " + operationCount(pkg) + " not converted",
			operationCount(pkg) == 0
				? "No EOperations need a Blockly behavior mapping."
				: "Operations require an explicit action-block, code-template or extension design.");
		row(out, "Smoke test",
			"▶ Optional",
			"Run `node scripts/smoke-test-generated.mjs` from the repository root to load samples automatically in a browser.");
		tableEnd(out);
	}

