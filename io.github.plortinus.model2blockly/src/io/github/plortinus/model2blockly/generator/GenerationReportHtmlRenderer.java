	private static String role(BlockTypeSpec bt) {
		if (bt.isAbstract()) return "Abstract type";
		switch (bt.getConnectionType()) {
			case NONE: return "Root block";
			case OUTPUT:
			case OUTPUT_TYPED: return "Expression/output block";
			case TYPED: return "Typed statement block";
			default: return "Statement block";
		}
	}

	private static String featureSummary(ClassDef cls, BlockTypeSpec bt) {
		int attributes = 0, containments = 0, references = 0, values = 0;
		for (Feature feature : cls.getFeatures()) {
			String name = feature.eClass().getName();
			if ("Attribute".equals(name)) attributes++;
			else if ("Containment".equals(name)) containments++;
			else if ("Reference".equals(name)) references++;
			else if ("ValueInput".equals(name)) values++;
		}
		if (bt == null) return "-";
		return attributes + " attributes, " + containments + " containments, "
			+ references + " references, " + values + " value inputs";
	}

